/**
 * Inline SVG icon used by the overlay primitives. Kept local to avoid pulling
 * the lucide-react bundle into deeply-nested overlay components — the X glyph
 * is the only icon overlays need.
 */
export function IconX({ size = 16 }: { size?: number }) {
  return (
    <svg
      width={size}
      height={size}
      viewBox="0 0 24 24"
      fill="none"
      stroke="currentColor"
      strokeWidth={2}
      strokeLinecap="round"
      strokeLinejoin="round"
      aria-hidden
    >
      <line x1="18" y1="6" x2="6" y2="18" />
      <line x1="6" y1="6" x2="18" y2="18" />
    </svg>
  );
}
